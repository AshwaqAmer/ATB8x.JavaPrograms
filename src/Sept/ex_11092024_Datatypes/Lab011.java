package Sept.ex_11092024_Datatypes;

public class Lab011 {
    public static void main(String[] args) {
        // Data Type variable_name = variable value

        // variable_name (Identifier) - Rules we have to follow
        /* 1. Identifiers can contain Alphabets, Digits, and two special chars i.e; Dollar($) and
              Underscore(_)
           2. First character can start with Alphabet or Dollar($) or Underscore(_)
           3. Keywords or reserved words can't be used as identifiers
        */
        // boolean 123 = true;
        boolean $ = true;
        boolean _= true;
        boolean This_can_be_used$ = true;
        boolean $124a = true;
        System.out.println($124a);
    }
}
