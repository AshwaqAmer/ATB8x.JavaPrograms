package Sept.ex_11092024_Datatypes;

public class Lab013 {
/* Constant are the special variable which cannot be modified during the program
execution. Constant is also called as final variables.
 */
public static void main(String[] args) {
    final int a = 10;
    final String a1 = "Ashwaq";
    final double d = 1250242.452;
    System.out.println('a');
    System.out.println("a1");
    System.out.println('d');
    }
}
